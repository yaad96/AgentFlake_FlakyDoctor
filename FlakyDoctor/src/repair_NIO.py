# ------------------------------------------------------------ NIO wrapper oracle
# A NIO (Non-Idempotent-Outcome) test passes the first time it runs but fails when run
# a second time in the same JVM, because it leaves shared state behind. There is no
# polluter (OD) and no NonDex seed (ID) to expose it — instead FlakyDoctor generates a
# tiny JUnit wrapper that invokes the victim twice in one JVM (full lifecycle each time)
# and asserts both invocations pass. The wrapper is the reproduction/verification oracle;
# the victim test method remains the only repair target (as for ID/OD).

def nio_wrapper_names(test):
    """'pkg.Class.method' -> the derived wrapper identifiers. The wrapper lives in the
    victim's own package (so it can reach a package-private victim class) and is named
    <Method>NioReproTest with a single test method runTwice."""
    victim_class = ".".join(test.split(".")[:-1])        # pkg.Class
    victim_method = test.split(".")[-1]                   # method
    pkg = ".".join(victim_class.split(".")[:-1])          # pkg (may be "")
    victim_class_simple = victim_class.split(".")[-1]     # Class
    method_cap = victim_method[0].upper() + victim_method[1:] if victim_method else victim_method
    wrapper_simple = method_cap + "NioReproTest"
    wrapper_fqcn = (pkg + "." + wrapper_simple) if pkg else wrapper_simple
    wrapper_test = wrapper_fqcn + ".runTwice"
    return pkg, victim_class_simple, victim_method, wrapper_simple, wrapper_fqcn, wrapper_test

def nio_wrapper_source(pkg, wrapper_simple, victim_class_simple, victim_method):
    lines = []
    if pkg:
        lines.append("package " + pkg + ";")
        lines.append("")
    lines += [
        "// AUTO-GENERATED by FlakyDoctor for NIO reproduction -- DO NOT EDIT.",
        "// Invokes " + victim_class_simple + "#" + victim_method + " twice in the same JVM (full",
        "// JUnit lifecycle each time). A non-idempotent test passes the first run and fails the",
        "// second; the fix target is the victim test, NOT this wrapper.",
        "import org.junit.Test;",
        "import org.junit.Assert;",
        "import org.junit.runner.JUnitCore;",
        "import org.junit.runner.Request;",
        "import org.junit.runner.Result;",
        "",
        "public class " + wrapper_simple + " {",
        "    @Test public void runTwice() throws Exception {",
        "        Request req = Request.method(" + victim_class_simple + ".class, \"" + victim_method + "\");",
        "        Result r1 = new JUnitCore().run(req);",
        "        Assert.assertTrue(\"first invocation should pass: \" + r1.getFailures(), r1.wasSuccessful());",
        "        Result r2 = new JUnitCore().run(req);",
        "        Assert.assertTrue(\"second invocation should pass (NIO assertion): \" + r2.getFailures(), r2.wasSuccessful());",
        "    }",
        "}",
        "",
    ]
    return "\n".join(lines)

def write_nio_wrapper(victim_file_path, test):
    """Generate (idempotently) the wrapper next to the victim's test file, so it shares the
    package and compiles into the same test module. Left untracked, so FlakyDoctor's between
    round `git stash` / `git checkout <victim>` never removes it."""
    pkg, victim_class_simple, victim_method, wrapper_simple, wrapper_fqcn, wrapper_test = nio_wrapper_names(test)
    wrapper_path = os.path.join(os.path.dirname(victim_file_path), wrapper_simple + ".java")
    if not os.path.exists(wrapper_path):
        utils.write_file(wrapper_path, nio_wrapper_source(pkg, wrapper_simple, victim_class_simple, victim_method))
        print("Generated NIO wrapper {} at {}".format(wrapper_fqcn, wrapper_path))
    wrapper_content = utils.read_file(wrapper_path)
    return wrapper_path, wrapper_fqcn, wrapper_test, wrapper_content

