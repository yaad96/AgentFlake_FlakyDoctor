def get_potential_API(test_content):
    # Lines that commonly LEAVE STATE behind between runs (non-idempotent outcome): writes to
    # static/shared fields, global JVM settings (default Locale/TimeZone, system properties),
    # singletons/caches, on-disk files, and registered listeners/hooks. Surfacing them helps
    # the model spot what the test must reset to become idempotent.
    potential_apis = {
        "static ":[], "System.setProperty":[], "System.clearProperty":[], "System.getProperties":[],
        "setProperty(":[], "clearProperty(":[], "Locale.setDefault":[], "TimeZone.setDefault":[],
        ".getInstance()":[], ".getDefault()":[], "getInstance(":[],
        ".put(":[], ".add(":[], ".set(":[], ".remove(":[], ".clear()":[], ".putIfAbsent(":[],
        "new File":[], ".createNewFile":[], ".delete()":[], ".deleteOnExit":[], ".mkdir":[],
        "Files.write":[], "Files.create":[], "Files.delete":[],
        ".register(":[], ".install(":[], ".addShutdownHook":[], ".addListener":[],
        "ThreadLocal":[], ".getAndSet":[], ".incrementAndGet":[], ".getAndIncrement":[],
    }
    if test_content != None:
        lines = test_content.split("\n")
        for api in potential_apis:
            for line in lines:
                if api in line:
                    potential_apis[api].append(line)
    return potential_apis

